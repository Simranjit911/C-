#include<iostream>
// C++ Program To Print A Message Multiple Times Using Loop
using namespace std;
int main(){
    int n;
    string s;
    cout<<"What do u want to print?";
    cin>>s;
    cout<<"How many times u want to print?";
    cin>>n;
    for(int i=0;i<=n;i++){
        cout<<s<<endl;

    }
    // cout<<"Completed execution";

}